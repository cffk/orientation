double pind(double ind, double delta) {
  const double s = 1.86; // sqrt(3.0); // 1.86; //sqrt(3.5);
  ind *= delta;
  switch (0) {
  case 0:
    return sinh(s * ind)/s;
    break;
  case 1:
    {
      double x = sinh(s * ind)/s;
      return 3 * x < 1 ? x : ind * (s/3.0)/asinh(s/3.0);
    }
    //    return tan(s * ind)/s;
    break;
  case 2:
    {
      double x = sinh(s * ind)/s;
      return 3 * x < 1 ? x :
	(1/3.0) + cosh(asinh(s/3.0)) * (ind - asinh(s/3)/s);
    }
    //    return tan(s * ind)/s;
    break;
  default:
    return ind;
  }
}

vector<Quaternion> CellSet(double delta) {
  vector<Quaternion> set;
  int n = 2*int(1/delta)+1;
  int maxind = -1;
  double maxslope = -100;
  double f = 0.0;
  for (int x = -n; x <= n; ++x) {
    double xf = pind(0.5*x, delta);
    if (abs(xf) >= sqrt(2.0) - 1 - f)
      continue;
    for (int y = -n; y <= n; ++y) {
      if ((x&1) != (y&1))	// All even or all odd
	continue;
      double yf = pind(0.5*y, delta);
      if (abs(yf) >= sqrt(2.0) - 1 - f)
	continue;
      for (int z = -n; z <= n; ++z) {
	if ((x&1) != (z&1))	// All even or all odd
	  continue;
	double zf = pind(0.5*z, delta);
	if (abs(zf) >= sqrt(2.0) - 1 - f)
	  continue;
	if (abs(xf) + abs(yf) + abs(zf) >= 1 - f)
	  continue;
	double s = xf + yf + zf;
	if (s > maxslope) {
	  maxind = set.size();
	  maxslope = s;
	}
	/*
	if (1 && x >= 0 && y >= 0 && z >=0 && x >= y && y >= z)
	  cout << setw(2) << x << " "
	       << setw(2) << y << " "
	       << setw(2) << z << endl;
	*/
	set.push_back(Quaternion(1.0, xf, yf, zf));
      }
    }
  }
  //  exit(0);
  /*
  cout << set[maxind].w() << " "
       << set[maxind].x() << " "
       << set[maxind].y() << " "
       << set[maxind].z() << endl;
  */
  return set;
}

void AddCell(const vector<Quaternion>& set, const Quaternion& tx,
	     double delta, PackSet& s, bool all) {
  double t = 2 * delta;
  for (size_t i = 0; i < set.size(); ++i) {
    Quaternion q(set[i]);
    q *= tx;
    double x = q.x()/q.w(), y = q.y()/q.w(), z = q.z()/q.w();
    if (all) {
      s.Add(q, 0, true);
      continue;
    }
    if (abs(x) > sqrt(2.0) - 1 + t ||
	abs(y) > sqrt(2.0) - 1 + t ||
	abs(z) > sqrt(2.0) - 1 + t ||
	abs(x) + abs(y) + abs(z) > 1 + sqrt(3.0) * t)
      continue;
    if (abs(x) < t && abs(y) < t && abs(z) < t) {
      s.Add(q, 0, true);
      continue;
    }
    if (!(x > sqrt(2.0) - 1 - t ||
	  y > sqrt(2.0) - 1 - t ||
	  z > sqrt(2.0) - 1 - t ||
	  x + y + z > 1 - sqrt(3.0) * t))
      continue;
    if (!(x > -t && y > -t && x > -t))
      continue;
    if (!(x > y - sqrt(2.0) * t && y > z - sqrt(2.0) * t))
      continue;
    s.Add(q, 0, true);
  }
}

